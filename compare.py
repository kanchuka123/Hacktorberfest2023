a = 5
b = 10

# Equality
is_equal = (a == b)

# Inequality
is_not_equal = (a != b)

# Less than
is_less_than = (a < b)

# Less than or equal
is_less_than_or_equal = (a <= b)

# Greater than
is_greater_than = (a > b)

# Greater than or equal
is_greater_than_or_equal = (a >= b)

# Displaying the results
print(f"a == b : {is_equal}")
print(f"a != b : {is_not_equal}")
print(f"a < b  : {is_less_than}")
print(f"a <= b : {is_less_than_or_equal}")
print(f"a > b  : {is_greater_than}")
print(f"a >= b : {is_greater_than_or_equal}")
